USE "Кудрицкий ПРОДАЖИ"
SELECT Наименование_товара
FROM     ЗАКАЗЫ
WHERE  (Дата_поставки > CONVERT(DATETIME, '1864-05-01 00:00:00', 102))
SELECT Наименование
FROM     ТОВАРЫ
WHERE  (Цена > 300 AND Цена <= 5000)
SELECT Заказчик
FROM ЗАКАЗЫ
WHERE	(Наименование_товара = 'Фрукты')
SELECT Наименование_товара, Дата_поставки
FROM     ЗАКАЗЫ
WHERE  (Заказчик = 'Англия')
ORDER BY Дата_поставки DESC
